    private void jbPrijavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPrijavaActionPerformed
        try {
            int TipKorisnika=10;
            Connection conn= DriverManager.getConnection("jdbc:derby://localhost:1527/AerodromDB","quantumair","quantumair");
            Statement st= conn.createStatement();
            ResultSet rs = st.executeQuery("select * from ZAPOSLENI");
            while(rs.next())
            {
                if(rs.getString(5).equals(jtxtUserName.getText()) && rs.getString(6).equals(jtxtPassword.getText()))
                {
                TipKorisnika=rs.getInt(7);
                break;
                }
                else{
                TipKorisnika=0;
                }
            }
            if(TipKorisnika == 0)
            {
            Greska prikaziGresku=new Greska();
prikaziGresku.setVisible(true);  
            }
            else if(TipKorisnika==1)
                    {
                        radnikPanel radnikovpanel=new radnikPanel();
radnikovpanel.setVisible(true);
        this.dispose();
                    }
            else if(TipKorisnika==2)
                    {
                       menadzerPanel menadzerovpanel=new menadzerPanel();
menadzerovpanel.setVisible(true);
        this.dispose(); 
                    }
            else if(TipKorisnika==3)
                    {
                       direktorPanel disinpanel=new direktorPanel();
disinpanel.setVisible(true);
        this.dispose(); 
                    }
        } catch (SQLException ex) {
            Logger.getLogger(Login_panel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbPrijavaActionPerformed

