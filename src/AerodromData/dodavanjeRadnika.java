    private void dodajRadnikaM() throws SQLException {
        Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/AerodromDB","quantumair","quantumair");
        PreparedStatement st= con.prepareStatement("insert into ZAPOSLENI(ID,IMEIPREZIME,DATUMZAPOSLENJA,DATUMPRESTANKARADA,KORISNICKOIME,LOZINKA,STATUS)values(?,?,?,?,?,?,?)");
        st.setString(1,jtxtID.getText());
        st.setString(2,jtxtImePrezime.getText());
        st.setString(3,jtxtDatumZaposlenja.getText());
        st.setString(4,jtxtDatumOtkaza.getText());
        st.setString(5,jtxtKorisnicko.getText());
        st.setString(6,jtxtLozinka.getText());
        st.setString(7,jtxtStatus.getText());
        st.executeUpdate();
    }
